static inline unsigned pixel32(int currcons, int cidx)
{
	cidx *= 3;
	return (palette[cidx] << 16) | (palette[cidx + 1] << 8)
		| palette[cidx + 2];
}

static inline unsigned pixel16(int currcons, int cidx)
{
	unsigned p;

	p = ((cidx << 10) & 0x7c00) + ((cidx << 5) & 0x03e0)
		+ (cidx & 0x1f);
	return (p << 16) | p;
}

